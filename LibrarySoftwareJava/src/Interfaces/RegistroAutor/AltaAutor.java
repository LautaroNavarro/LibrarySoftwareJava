    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Autor autor = new Autor();
        autor.setNombre(txt_nombre.getText());
        autor.setNacionalidad(txt_nacionalidad.getText());
        try{
           DateFormat format = new SimpleDateFormat("yyyy/MM/dd", Locale.ENGLISH);
           Date date = format.parse(txt_fechaNacimiento.getText());
           autor.setFechaNacimiento(date);
           controller.guardarAutor(autor);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"No has ingresado una fecha v√°lida");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

