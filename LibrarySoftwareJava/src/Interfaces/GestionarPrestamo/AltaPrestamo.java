    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //ACa debo obtener Libro y Lector
       

        
        
        //Validar No mas de 3 prestamos para un lector
        if (cbx_lectores.getSelectedIndex() < 0) {
            //Validar Lector Seleccionado
            JOptionPane.showMessageDialog(null, "Debe Seleccionar un lector");
        } else {
            Lector lectorSeleccionado = (Lector)cbx_lectores.getSelectedItem();
            int filaSeleccionada = tbx_libros.getSelectedRow();
             //vALIDAR Libro SELECCIONADO
            if (filaSeleccionada < 0) {
                JOptionPane.showMessageDialog(null, "Debe Seleccionar un libro");
            } else {
                Libro libroSeleccionado = librosFiltrados.get(filaSeleccionada);
               //Validar copiaLibroDisponible
                if (libroSeleccionado.copiasDisponibles() == 0) {
                     JOptionPane.showMessageDialog(null, "El libro seleccionado no posee copias disponibles");
                }else{
                    //Validar Lector sin multa
                   List<Multa> multasVigentes =  this.controller.obtenerMultasVigentesPorLector(lectorSeleccionado);
                    if (!multasVigentes.isEmpty()) {
                          JOptionPane.showMessageDialog(null, "El Lector se encuantra multado a la fecha");
                    }else{
                        if (lectorSeleccionado.prestamosVigentes().size() >= 3) {
                              JOptionPane.showMessageDialog(null, "El Lector alcanzo el máximo de prestamos");
                        }else{
                            Prestamo p = new Prestamo();
                            p.setFechaPrestamo(new Date());
                            Copia c = libroSeleccionado.obtenerSiguienteCopiaDisponible();
                            p.setCopia(c);
                            c.setEstado(Copia.PRESTADO);
                            lectorSeleccionado.agregarPrestamo(p);
                            this.controller.registrarPrestamo(p);
                            JOptionPane.showMessageDialog(null, "El prestamo se registro Correctamente");
                          //  p.setCopia(copia);
                        }
                    }
                }
            }
        }


    }//GEN-LAST:event_jButton2ActionPerformed

