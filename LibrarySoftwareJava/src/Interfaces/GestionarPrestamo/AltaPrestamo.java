    private void refrescarTablaLibros() {

        Autor autorSeleccionado = null;
        TipoLibro tipoLibroSeleccionado = null;

        if (cbx_autores.getSelectedIndex() >= 0) {
            autorSeleccionado = (Autor) cbx_autores.getSelectedItem();

        }
        System.out.println("cbtpl" + cbx_tipoLibro.getSelectedItem());
        if (cbx_tipoLibro.getSelectedIndex() >= 0) {
            tipoLibroSeleccionado = (TipoLibro) cbx_tipoLibro.getSelectedItem();

        }

        //Recien si tengo seleccionado Autory tipo de libro actualizo
        if (autorSeleccionado != null && tipoLibroSeleccionado != null) {
            librosFiltrados = controller.buscarLibrosYAutorTipoLibro(autorSeleccionado, tipoLibroSeleccionado);

        } else {
            librosFiltrados = new ArrayList<>();
        }
        tbx_libros.setModel(new LibrosPrestamosModeloTabla(librosFiltrados));
    }

